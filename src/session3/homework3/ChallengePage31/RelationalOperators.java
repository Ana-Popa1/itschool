//Write a Java program that takes two numbers from the user and uses relational operators to display whether the first number is greater than, less than, or equal to the second number.
//Scrieți un program Java care ia două numere de la utilizator și utilizează operatori relaționali pentru a afișa
// dacă primul număr este mai mare decât, mai mic sau egal cu al doilea număr.
public class RelationalOperators {
    public static void main(String[] args){
        int a = 10;
        int b = 7;
        System.out.println("a > b : " + (a>b));
        System.out.println("a < b : " + (a<b));
        System.out.println("a = b : " + (a==b));
        System.out.println("a != b : " + (a!=b));
    }
}
